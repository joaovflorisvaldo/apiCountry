package model;

public class Pais {

    private int codio;
    private String descricao;

    public Pais() {
        this.codio = codio;
        this.descricao = descricao;
    }

    public int getCodio() {
        return codio;
    }

    public void setCodio(int codio) {
        this.codio = codio;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }


}
